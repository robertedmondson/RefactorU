angular.module("GoTApp", [])
    .controller("GoTController", gController);

gController.$inject = ['$http'];

function gController($http) {
    var gCtrl = this;

    gCtrl.greeting = "Winter is coming...";

    gCtrl.getCharacters = function() {
        console.log("Getting list of characters...");

        $http.get('http://anapioficeandfire.com/api/characters?page=' + gCtrl.page + '&pageSize=50')
        .then(function(response){
            console.log("Response from API: ", response.data);
            gCtrl.charList = response.data;
        });
    };

    gCtrl.getCharacter = function() {
        console.log("Getting character named " + gCtrl.name + "...");

        $http.get('http://anapioficeandfire.com/api/characters?name=' + gCtrl.name)
        .then(function(response){
            console.log("Response from API: ", response.data);
            gCtrl.charData = response.data;
        });
    };
}





































